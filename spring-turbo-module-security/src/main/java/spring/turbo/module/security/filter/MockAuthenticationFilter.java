package spring.turbo.module.security.filter;

import jakarta.servlet.FilterChain;
import jakarta.servlet.ServletException;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import org.springframework.lang.Nullable;
import org.springframework.security.core.context.SecurityContextHolder;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.web.filter.OncePerRequestFilter;
import spring.turbo.module.security.authentication.Authentication;
import spring.turbo.module.security.token.Token;
import spring.turbo.util.Asserts;

import java.io.IOException;

/**
 * @author 应卓
 * @since 2.1.0
 */
public class MockAuthenticationFilter extends OncePerRequestFilter {

    private final Authentication authentication;

    public MockAuthenticationFilter(UserDetails mockUserDetails) {
        this(mockUserDetails, null);
    }

    public MockAuthenticationFilter(UserDetails mockUserDetails, @Nullable Token token) {
        Asserts.notNull(mockUserDetails);
        this.authentication = new Authentication(mockUserDetails, token);
    }

    @Override
    protected void doFilterInternal(HttpServletRequest request, HttpServletResponse response, FilterChain filterChain) throws ServletException, IOException {
        SecurityContextHolder.getContext().setAuthentication(this.authentication);
        filterChain.doFilter(request, response);
    }

}
