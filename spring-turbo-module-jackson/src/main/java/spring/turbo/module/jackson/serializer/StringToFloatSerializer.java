package spring.turbo.module.jackson.serializer;

import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.databind.JsonSerializer;
import com.fasterxml.jackson.databind.SerializerProvider;
import org.springframework.lang.Nullable;
import spring.turbo.util.NumberParseUtils;

import java.io.IOException;

/**
 * @author 应卓
 * @since 1.0.12
 */
public class StringToFloatSerializer extends JsonSerializer<String> {

    @Override
    public void serialize(@Nullable String value, JsonGenerator gen, SerializerProvider serializers) throws IOException {

        if (value == null) {
            gen.writeNull();
            return;
        }

        final Float n = NumberParseUtils.parse(value, Float.class);
        gen.writeNumber(n);
    }

}
